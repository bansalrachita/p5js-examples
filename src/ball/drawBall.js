var drawBall = function(p5, img, x, y, r, s) {
  s = p5.map(s, 0, 100, 0.1, 1.9);
  var rx = 32 * (2 - s);
  var ry = 32 * s;

  // Shadow
  p5.push();
  p5.translate(x + 1, y - ry / 2 + 1);
  p5.rotate(r);
  p5.noStroke();
  p5.fill(0, 0, 0, 60);
  p5.ellipse(0, 0, rx - 3, ry - 3);
  p5.pop();

  // Ball
  p5.push();
  p5.translate(x, y - ry / 2);
  p5.rotate(r);
  p5.image(img, -rx / 2, -ry / 2, rx, ry);
  p5.pop();
};

export const animateBall = p5 => {
  let ball = p5.loadImage('ball.png');
  p5.background(0);
  p5.frameRate(24);
  p5.fill(230, 240, 255);
  p5.noStroke();
  p5.rect(-1, 75, p5.width + 2, 250);
  p5.fill(120, 200, 160);
  p5.rect(-1, 305, p5.width + 2, 20);
  p5.strokeWeight(2);
  p5.stroke(28, 173, 123);
  p5.line(-1, 305, p5.width + 1, 305);

  var x = xArray[p5.frameCount % n];
  var y = yArray[p5.frameCount % n];
  var r = rArray[p5.frameCount % n];
  var s = sArray[p5.frameCount % n];
  drawBall(p5, ball, x, y, r, s);
};

